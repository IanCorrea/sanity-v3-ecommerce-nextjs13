function classNames(...classes: any) {
  return classes.filter(Boolean).join(' ')
} 

function getSelectedClass(color: any) {
  const findColor = productDummy.colors.find(obj => obj.name === color);
  return findColor ? findColor.selectedClass : 'ring-gray-900'
}

function getClass(color: any) {
  const findColor = productDummy.colors.find(obj => obj.name === color);
  return findColor ? findColor.class : 'bg-gray-900'
}

